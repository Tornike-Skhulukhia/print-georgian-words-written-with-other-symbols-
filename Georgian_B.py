def write_in_file(data, destination = False, open_after = False):
    '''
        Specifically for after previous function use;
        
        Gets data and writes it in a txt file called result.txt,
        file is saved in directory which is included as a second argument (default = current working directory);

        If third argument is True(default = False), text file will be opened with default program.

        Example:
            step 1. text = Georgian_B("gamarjoba", " ", "*", True, 0)   or shorter  text = Georgian_B("gamarjoba")
            step 2. write_in_file(text)
        After these 2 steps, file shuld be created in working directory and opened with default program (such as notepad)
    
    '''
    #import
    import os
    #Name of file
    filename = "result.txt"
    #Folder to save file
    if not destination: #if destination is not included, current working directory will be used
        destination_to_use = os.getcwd()
    else:
        destination_to_use = destination
    
    #full path to file
    file_path = os.path.join(destination_to_use, filename)
    
    #write data in file
    with open(file_path, "wb+") as f: #open in binary mode 
        for line in data:
            f.write((line.encode("utf-8") + os.linesep.encode("utf-8")))
    #Print
    print("\n","| File {} was created successfully in {} | ".format(filename, destination_to_use).center(80,"-"), "\n")
    #Open file, if open_after argument is True
    if open_after:
        os.popen(file_path)

