def Georgian_B(text, separator=" ", shaper = "*",print_text = False, wait_time = 0):
    '''
       Returns georgian letters/words and few other previously defined symbols
       with any character as a separator(default = " ") and shaper(default = "*");
       For best results should be used only with ascii letters, but others are also supported.
       
       If print_text argument is True(default = False), text is also printed,
       in this case, wait_time argument(default = 0) controls how much seconds to wait after each line was printed.
      
       For example, result of Georgian_B("gamarjoba", " ", "*", True, 0) will be:
       
                                                                                      *                                *                        
                                     *******                       *                  *                                *                        
                                    *       *                      *                  ***                                *                      
                                    *       *                      *                    *                                *                      
                                            *                      *                 *  *                                *                      
     *****                                  *                      *                  * *                                *                      
    *      *            *            ********           *           ********            *           ***   ***         *******           *       
    *      *             *          *       *            *         *        *           * *        *   * *   *       *       *           *      
         *                *         *       *             *        *        *           *  *      *     *     *      *       *            *     
      *****                *        *       *              *       *        *           **        *     *     *      *       *             *    
    *      *        *      *        *       *       *      *       *        *              *      *           *       *******       *      *    
  *         *        ******          *******         ******        *        *               *      *         *                       ******     
  *          *                                                                              *                                                   
  *          *                                                                      *       *                                                   
   *        *                                                                       *       *                                                   
    *      *                                                                         ******                                                     
      ****                                                                                                                                      


    Using write_in_file function in same module, you can write this data in a simple text file.
    This is especially useful, if sentence is wide enough to be not fully visible on screen.
    
    '''
    
    #import
    import time, os
    #Get characters from text
    letters_raw, letters = [], []
    defined_symbols = {".":"dot__", ",":"comma__"," ":"space__", "!":"exclamation_mark__", "?":"question_mark__"}
    defined_letters = {"a__", "b__", "g__", "d__", "e__", "v__", "z__", "T__", "i__", "k__", "l__", "m__", "n__", "o__", "p__", "J__", "r__", "s__", "t__", "u__", "f__", "q__", "R__", "y__", "S__", "C__", "c__", "Z__", "w__", "W__", "x__", "j__", "h__"}
    #add letters and symbols, check that each given letter is in list of letters or symbols
    for letter in list(text):
        letter_to_add = ""
        if (letter + "__") in defined_letters:
            letter_to_add = letter + "__"
        elif (letter) in defined_symbols:
            letter_to_add = defined_symbols[letter]
        else:
            print("| Letter {} is not defined! |".format(repr(letter)).center(40))
            return
        letters.append(letter_to_add)
    #define variables
    printText = letters[0]  + "[index]"
    full_text = []
    #Append print text the way we need to use with eval function later
    for letter in letters[1:]:
        printText += "+ " + letter + "[index]"
    #generate result
    for index in range(20):   # 20 --> number of lines in each letter/symbol
        printTextFinished = eval(printText)
        printTextFinished = printTextFinished.replace(".",separator).replace("*",shaper)  #replace default characters, if needed
        #add line to result
        full_text.append(printTextFinished)
    #print result if print_text argument is True
    if print_text:
        for line in full_text:
            print(line)
            time.sleep(wait_time)  #time to wait after each line
    #return result
    return full_text

